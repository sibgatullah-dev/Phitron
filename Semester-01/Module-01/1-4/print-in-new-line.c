#include <stdio.h>

int main()
{
    printf("Hello\n");// \n means print in a new line. It's called an escape sequence.
    // The \n escape sequence is used to insert a new line in the output.
    printf("world\n");
    printf("Country\tBangladesh\n");// \t means tab space. It's called an escape sequence.
    return 0;
}